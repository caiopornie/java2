package math;

public class PowSqrt {

	public static void main(String[] args) {
		int a = 5;
		int b = 2;
		double c = Math.pow(a,b);
		
		//pow retorna um double resultado do primeiro parametro elevado ao segundo
		System.out.println(Math.pow(a, b));
		
		//sqrt retorna um double resultado da raiz quadrada do parametro
		System.out.println(Math.sqrt(c));
	}
}
