package math;

public class MaxMin {
	
	public static void main(String[] args) {
		
	int a = 1;
	int b = 2;
	
	//A função max retorna o valor do maior entre os 2 argumentos passados
	System.out.println(Math.max(a, b));

	//A função min retorna o valor do menor entre os 2 argumentos passados
	System.out.println(Math.min(a, b));
	}
}
