package math;

public class ToRadianToDegrees {

	public static void main(String[] args) {
		int angulo = 180;
		double radiano = 3.14;
		
		//toRadians converte uma medida circular em graus para radianos
		System.out.println(Math.toRadians(angulo));
		
		//toDegrees converte uma medida em radiano para graus
		System.out.println(Math.toDegrees(radiano));
	
	}
}
